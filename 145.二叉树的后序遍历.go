/**
 * Definition for a binary tree node.
 * type TreeNode struct {
 *     Val int
 *     Left *TreeNode
 *     Right *TreeNode
 * }
 */
func postorderTraversal(root *TreeNode) []int {
	res := []int{}
	if root == nil {
		return nil
	}
	st := []*TreeNode{}
	st = append(st, root)
	for len(st) > 0 {
		tmp := st[len(st)-1]
		st = st[:len(st)-1]
		res = append(res, tmp.Val)
		if tmp.Left != nil {
			st = append(st, tmp.Left)
		}
		if tmp.Right != nil {
			st = append(st, tmp.Right)
		}
	}
	i, j := 0, len(res)-1
	for i < j {
		res[i], res[j] = res[j], res[i]
		i++
		j--
	}
	return res
}

